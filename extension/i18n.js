// Resolve a translation with fallback: chosen lang -> English -> the key itself.
function cimeaT(key, lang) {
  var L = (lang === "ru") ? "ru" : "en";
  var table = CIMEA_I18N[L] || CIMEA_I18N.en;
  if (table && table[key] != null) return table[key];
  if (CIMEA_I18N.en[key] != null) return CIMEA_I18N.en[key];
  return key;
}

// Pick a sensible default language from the browser UI language.
function cimeaDefaultLang() {
  try {
    var ui = (chrome.i18n && chrome.i18n.getUILanguage && chrome.i18n.getUILanguage()) || "";
    return /^ru\b/i.test(ui) ? "ru" : "en";
  } catch (_) { return "en"; }
}

if (typeof self !== "undefined") {
  self.CIMEA_I18N = CIMEA_I18N;
  self.cimeaT = cimeaT;
  self.cimeaDefaultLang = cimeaDefaultLang;
}
